async function renderLessons() {
  try {
    const resp = await fetch('./manifest.json');
    if (!resp.ok) throw new Error('Не найден manifest.json');
    const manifest = await resp.json();

    const valuesDiv = document.querySelector('.values');
    valuesDiv.innerHTML = '';

    manifest.forEach((item) => {
      const card = document.createElement('div');
      card.className = 'lesson-card';
      card.onclick = function () {
        openLesson(item.file, item.heading);
      };

      const img = document.createElement('img');
      img.className = 'lesson-img';
      img.src = item.image || './imgs/placeholder.png'; 
      img.alt = item.heading;

      img.onerror = function () {
        this.src = './imgs/placeholder.png';
      };

      const info = document.createElement('div');
      info.className = 'lesson-info';
      info.innerHTML = '<h3>' + item.heading + '</h3>';

      card.appendChild(img);
      card.appendChild(info);
      valuesDiv.appendChild(card);
    });
  } catch (e) {
    console.error(e);
    const valuesDiv = document.querySelector('.values');
    if (valuesDiv) {
      valuesDiv.innerHTML = '<p style="color:#fff;">Ошибка загрузки списка уроков.</p>';
    }
  }
}


async function openLesson(file, heading){
  try{
    const response = await fetch(`./lessons/${file}`);
    if(!response.ok) throw new Error('Занятие не найдено');
    const content = await response.text();
    document.getElementById('values-page').style.display='none';
    document.getElementById('literature-content').style.display='none';
    document.getElementById('lesson-content').style.display='block';
    const titleEl = document.getElementById('lesson-title');
titleEl.innerText = heading;
titleEl.classList.add('lesson-heading-colored');

    document.getElementById('lesson-text').innerHTML = content;
    ['theory','practice','desc','appendix'].forEach(function(s){
      const el = document.getElementById(s);
      if(!el) return;
      if(s==='theory'){ el.style.display='block'; el.classList.remove('hidden'); }
      else { el.style.display='none'; el.classList.add('hidden'); }
    });
    window.scrollTo({top:0, behavior:'auto'});
  }catch(err){
    console.error(err);
    document.getElementById('values-page').style.display='none';
    document.getElementById('lesson-content').style.display='block';
    document.getElementById('lesson-title').innerText = heading || 'Занятие';
    document.getElementById('lesson-text').innerHTML = '<p>Контент занятия не найден. Помести файл в папку <code>lessons/</code>.</p>';
  }
}

